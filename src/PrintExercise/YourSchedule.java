package PrintExercise;

public class YourSchedule {

	public static void main(String[] args) {
		
		String favoriteBreakfast = "Bacon and Eggs";
		String favoriteLunch = "Pizza";
		String favoriteDinner = "Bacon Cheese Fries";
		String favoriteDessert = "Tiramisu";
		
		
		System.out.println("+---------------------------------------------------------------------------+");
		System.out.printf("| %40s | %30s |%n", "Favorite breakfast", favoriteBreakfast);
		System.out.printf("| %40s | %30s |%n", "Favorite lunch", favoriteLunch);
		System.out.printf("| %40s | %30s |%n", "Favorite dinner", favoriteDinner);
		System.out.printf("| %40s | %30s |%n", "Favorite dessert", favoriteDessert);
		System.out.println("+---------------------------------------------------------------------------+");
	}

}
